package conditionHandling;

public class Divisible {

	public Boolean Divisibleby5(int a)
	{
		if (a%5==0)
		{	return true;
		}
		else
		{
			return false;
		}
	}
	public Boolean Divisibleby11(int a)
	{
		if (a%11==0)
		{	return true;
		}
		else
		{
			return false;
		}
	}
}
